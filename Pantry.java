public class Pantry {   

   Jam jar1;
   Jam jar2;
   Jam jar3;
   public Pantry(Jam j1, Jam j2, Jam j3) {
      jar1 = j1;
      jar2 = j2;
      jar3 = j3;
   }
   
   public void spreadJam(double amount) {
      this.spreadJam(amount);
   }
   
   public String toString() {
      return "Jar 1: " + jar1.toString() + "Jar 2: " + jar2.toString() + "Jar 3: " + jar3.toString();
   }

}